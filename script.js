function toggleCourseGroup(courseGroup) {
    courseGroup.classList.toggle('expanded');
}

function openVideoModal(video, series, index, seriesTitle = '') {
    modalTitle.textContent = `${seriesTitle} - ${video.name}`; 
    modalVideo.src = video.path;
    modalDescription.textContent = ''; 

    currentSeries = series; 
    currentEpisodeIndex = index; 

    if (currentSeries && currentSeries.length > 1) {
        modalSeriesNav.style.display = 'flex';
        updateSeriesNavigation();
    } else {
        modalSeriesNav.style.display = 'none';
    }

    modal.setAttribute('aria-hidden', 'false');
    document.body.style.overflow = 'hidden';
    modalVideo.play(); 
}

function closeVideoModal() {
    modalVideo.pause();
    modalVideo.currentTime = 0; 
    modalVideo.src = ''; 
    modal.setAttribute('aria-hidden', 'true');
    document.body.style.overflow = ''; 
    modalSeriesNav.style.display = 'none';
    currentSeries = []; 
    currentEpisodeIndex = -1;
    modalEpisodeSelect.innerHTML = ''; 
}

function updateSeriesNavigation() {
    if (!currentSeries || currentSeries.length <= 1) return;

    modalEpisodeInfo.textContent = `Episode ${currentEpisodeIndex + 1} of ${currentSeries.length}`;

    modalPrevBtn.disabled = currentEpisodeIndex <= 0;
    modalNextBtn.disabled = currentEpisodeIndex >= currentSeries.length - 1;

    modalEpisodeSelect.innerHTML = ''; 
    currentSeries.forEach((vid, index) => {
        const option = document.createElement('option');
        option.value = index;
        option.textContent = `Episode ${index + 1}: ${vid.name}`;
        if (index === currentEpisodeIndex) {
            option.selected = true;
        }
        modalEpisodeSelect.appendChild(option);
    });
}

function goToEpisode(index) {
    if (currentSeries && index >= 0 && index < currentSeries.length) {
        const nextVideo = currentSeries[index];
        openVideoModal(nextVideo, currentSeries, index);
    }
}

modalCloseBtn.addEventListener('click', closeVideoModal);
modal.addEventListener('click', (e) => { 
    if (e.target === modal) {
        closeVideoModal();
    }
});

modalPrevBtn.addEventListener('click', () => {
    if (currentEpisodeIndex > 0) {
        goToEpisode(currentEpisodeIndex - 1);
    }
});

modalNextBtn.addEventListener('click', () => {
    if (currentEpisodeIndex < currentSeries.length - 1) {
        goToEpisode(currentEpisodeIndex + 1);
    }
});

modalEpisodeSelect.addEventListener('change', (e) => {
    goToEpisode(parseInt(e.target.value, 10));
});

function toggleNav() {
    slideNav.classList.toggle('open');
    overlay.classList.toggle('visible');
    const isExpanded = slideNav.classList.contains('open');
    menuToggle.setAttribute('aria-expanded', isExpanded);
    slideNav.setAttribute('aria-hidden', !isExpanded);
}

function closeNav() {
    slideNav.classList.remove('open');
    overlay.classList.remove('visible');
    menuToggle.setAttribute('aria-expanded', false);
    slideNav.setAttribute('aria-hidden', true);
}

menuToggle.addEventListener('click', toggleNav);
closeNavBtn.addEventListener('click', closeNav);
overlay.addEventListener('click', closeNav); 

function showSection(sectionId) {
    contentSections.forEach(section => {
        section.classList.remove('active');
        section.setAttribute('aria-hidden', true);
    });
    const activeSection = document.querySelector(sectionId);
    if (activeSection) {
        activeSection.classList.add('active');
        activeSection.setAttribute('aria-hidden', false);
        activeSection.querySelector('h2, h3')?.focus();
    }
}

navLinks.forEach(link => {
    link.addEventListener('click', (e) => {
        e.preventDefault();
        const sectionId = link.getAttribute('href');
        history.pushState(null, '', sectionId);
        showSection(sectionId);
        closeNav(); 
    });
});

